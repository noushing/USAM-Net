# 2. U-SAMNET MODEL DEFINITION
# ============================================================
def usamnet_model(input_size=(256, 256, 1)):
    """
    U-SAMNet: Uncertainty-aware Self-Attention Multi-task Network.

    Architecture:
        - Shared U-Net encoder-decoder backbone
        - UGA block at bottleneck for uncertainty-guided attention
        - Three supervised output heads: segmentation,
          classification, denoising
        - Separate inference model for MC-dropout uncertainty

    Args:
        input_size: Input image dimensions (H, W, C)

    Returns:
        model: Training model with 3 supervised outputs
        inference_model: Inference model with 4 outputs
                        including uncertainty
    """
    inputs = Input(input_size)

    # ---- Encoder ----
    # Level 1: [B, 128, 128, 64]
    x = Conv2D(64, 3, activation='relu', padding='same')(inputs)
    x = BatchNormalization()(x)
    x = Conv2D(64, 3, activation='relu', padding='same')(x)
    skip1 = BatchNormalization()(x)

    # Level 2: [B, 64, 64, 128]
    x = MaxPooling2D()(skip1)
    x = Conv2D(128, 3, activation='relu', padding='same')(x)
    x = BatchNormalization()(x)
    x = Conv2D(128, 3, activation='relu', padding='same')(x)
    skip2 = BatchNormalization()(x)

    # Bottleneck: [B, 32, 32, 256]
    x = MaxPooling2D()(skip2)
    x = Conv2D(256, 3, activation='relu', padding='same')(x)
    x = BatchNormalization()(x)

    # ---- UGA Block ----
    # U_map in R^{32x32x1} from MC-dropout on bottleneck
    bottleneck_unc = Dropout(0.5)(x, training=True)
    uncertainty_map = Conv2D(1, 1,
                             activation='sigmoid')(bottleneck_unc)
    x = uag_block(x, uncertainty_map)

    # ---- Decoder ----
    x = UpSampling2D()(x)
    x = Concatenate()([x, skip2])
    x = Conv2D(128, 3, activation='relu', padding='same')(x)
    x = BatchNormalization()(x)

    x = UpSampling2D()(x)
    x = Concatenate()([x, skip1])
    x = Conv2D(64, 3, activation='relu', padding='same')(x)
    decoder_features = BatchNormalization()(x)

    # ---- Supervised Output Heads ----
    # Segmentation head: pixel-wise pore mask
    seg_output = Conv2D(1, 1, activation='sigmoid',
                        name='segmentation')(decoder_features)

    # Classification head: image-level pore presence
    cls_branch = GlobalAveragePooling2D()(decoder_features)
    cls_branch = Dense(64, activation='relu')(cls_branch)
    cls_branch = Dropout(0.5)(cls_branch)
    cls_output = Dense(1, activation='sigmoid',
                       name='presence')(cls_branch)

    # Denoising head: image reconstruction for regularization
    denoise_output = Conv2D(1, 1, activation='sigmoid',
                            name='denoised')(decoder_features)

    # Training model: 3 supervised outputs only
    # Uncertainty is NOT a supervised target
    model = Model(inputs,
                  outputs=[seg_output, cls_output,
                           denoise_output])

    # Inference model: includes MC-dropout uncertainty output
    # Uncertainty estimated from predictive variance (T=10 passes)
    dropout_unc = Dropout(0.5)(decoder_features, training=True)
    unc_output = Conv2D(1, 1, activation='sigmoid',
                        name='uncertainty')(dropout_unc)
    inference_model = Model(inputs,
                            outputs=[seg_output, unc_output,
                                     cls_output, denoise_output])

    return model, inference_model

