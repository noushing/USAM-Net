# 1. UNCERTAINTY GUIDANCE ATTENTION (UGA) BLOCK
# ============================================================
def uag_block(x, uncertainty_map, ratio=8):
    """
    Uncertainty Guidance Attention Block.
    Suppresses channel attention weights using inverted
    epistemic uncertainty estimates.

    Args:
        x: Input feature tensor [B, H, W, C]
        uncertainty_map: Spatial uncertainty map [B, H, W, 1]
        ratio: Channel reduction ratio for SE-style attention

    Returns:
        Modulated feature tensor [B, H, W, C]
    """
    channel = x.shape[-1]
    shared_dense_one = Dense(channel // ratio, activation='relu')
    shared_dense_two = Dense(channel)

    # Channel Attention via GAP and GMP
    avg_pool = GlobalAveragePooling2D()(x)
    max_pool = GlobalMaxPooling2D()(x)
    avg_out = shared_dense_two(shared_dense_one(avg_pool))
    max_out = shared_dense_two(shared_dense_one(max_pool))

    # A_c in R^{1x1xC}
    channel_attention = Add()([avg_out, max_out])
    channel_attention = Activation('sigmoid')(channel_attention)
    channel_attention = Reshape((1, 1, channel))(channel_attention)

    # A_UGA = A_c * (1 - U_map) via broadcasting
    # (1-U_map) in R^{HxWx1} broadcasts across C channels
    # A_c in R^{1x1xC} broadcasts across HxW spatial locations
    attention_weight = Multiply()([channel_attention,
                                   1 - uncertainty_map])
    return Multiply()([x, attention_weight])

