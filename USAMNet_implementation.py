# 7. MC-DROPOUT UNCERTAINTY ESTIMATION
# ============================================================
def mc_dropout_uncertainty(inference_model, X, T=10):
    """
    Estimate epistemic uncertainty via MC-dropout.

    Performs T stochastic forward passes with dropout active
    and computes predictive variance as uncertainty estimate.

    U = (1/T) * sum((S_t - S_mean)^2)

    Args:
        inference_model: Model with dropout active at inference
        X: Input images [N, H, W, 1]
        T: Number of stochastic forward passes

    Returns:
        mean_pred: Mean segmentation prediction [N, H, W, 1]
        uncertainty: Predictive variance [N, H, W, 1]
    """
    preds = np.stack(
        [inference_model.predict(X, verbose=0)[0]
         for _ in range(T)],
        axis=0)
    uncertainty = np.var(preds, axis=0)
    mean_pred   = np.mean(preds, axis=0)
    return mean_pred, uncertainty

