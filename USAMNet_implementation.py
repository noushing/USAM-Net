# 9. BOOTSTRAP CONFIDENCE INTERVALS
# ============================================================
def bootstrap_ci(y_true, y_pred, n_bootstrap=1000, ci=95):
    """
    Compute bootstrap confidence intervals for metrics.

    Args:
        y_true: Ground truth labels
        y_pred: Predicted labels
        n_bootstrap: Number of bootstrap resamples
        ci: Confidence interval percentage

    Returns:
        Dictionary with mean, std, lower, upper for each metric
    """
    metrics = {'accuracy':  [],
               'precision': [],
               'recall':    [],
               'f1':        [],
               'iou':       []}

    for _ in range(n_bootstrap):
        indices = resample(np.arange(len(y_true)),
                           replace=True)
        y_t = y_true[indices]
        y_p = y_pred[indices]

        if len(np.unique(y_t)) < 2:
            continue

        cm = confusion_matrix(y_t, y_p)
        tn, fp, fn, tp = cm.ravel()

        metrics['accuracy'].append(accuracy_score(y_t, y_p))
        metrics['precision'].append(
            precision_score(y_t, y_p, zero_division=0))
        metrics['recall'].append(
            recall_score(y_t, y_p, zero_division=0))
        metrics['f1'].append(
            f1_score(y_t, y_p, zero_division=0))
        metrics['iou'].append(
            tp / (tp + fp + fn)
            if (tp + fp + fn) > 0 else 0)

    alpha = (100 - ci) / 2
    results = {}
    for metric, values in metrics.items():
        values = np.array(values)
        results[metric] = {
            'mean':  np.mean(values),
            'std':   np.std(values),
            'lower': np.percentile(values, alpha),
            'upper': np.percentile(values, 100 - alpha)
        }
    return results

